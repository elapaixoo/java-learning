package estruturascondicionais01;

public class RegrasDeNomeacao {

	public static void main(String[] args) {
		// REGRAS DE NOMEAÇÃO GERAL
		
		/*
		
		✔️ NOMEAÇÃO DE CLASSES
		
		Regra:
			
			Começa com letra maiúscula

			Se o nome tiver mais de uma palavra → CamelCase com cada palavra iniciando em maiúscula

			Exemplos:

				Main
	
				Cliente
	
				MinhaPrimeiraClasse
	
				CalculadoraImc
	
				❌ Não pode começar com número
				❌ Não coloque acentos, nem ç
		
		
		
		✔️ NOMEAÇÃO DE PACOTES
		
		Regra:
		
			Sempre tudo minúsculo

			Sem acento

			Palavras separadas por ponto (.)

			Exemplos:

				br.com.nome
	
				meu.projeto
	
				calculadora.imc
	
				utilidades
	
				❌ nunca use maiúscula em pacotes
				❌ não use underline
				❌ não coloque números como primeira coisa (pode no meio)
		
		
			
		✔️ NOMEAÇÃO DE MÉTODOS (funções)

		Regra:

			Começa com minúscula
	
			Usa camelCase se tiver mais palavras
	
			Nome deve indicar ação
	
			Exemplos:
	
				calcularMedia()
		
				imprimirResultado()
		
				somarNumeros()
		
		

		✔️ NOMEAÇÃO DE VARIÁVEIS

		Mesma regra dos métodos:
		
			Começa com minúscula
	
			camelCase
	
			Nome claro
	
			Exemplos:
	
				idade
		
				nomeCompleto
		
				valorTotal
			
			

		✔️ NOMEAÇÃO DE CONSTANTES

		Regra:

			Todas MAIÚSCULAS
	
			Separadas por underline
	
			Exemplos:
	
				TAXA_JUROS
		
				IDADE_MINIMA
		
				PI
			
			

		✔️ NOMEAÇÃO DE PROJETOS (na IDE)

		Flexível, mas o mais comum:

			Não usar espaços
	
			CamelCase ou tudo minúsculo
	
			Exemplos:
	
				java-learning
		
				projetoJava
		
				estruturaDeDados
		
				cursoJava
		
		*/
	}

}
